package mx.com.televisa.derechocorporativo.model;

import java.io.Serializable;

public class DetailApoderadosDTO implements Serializable{
	private static final long serialVersionUID = 1L;
	
	private String apoderados;
	private String actosDominio;
	private String actosAdministracion;
	private String titulosCredito;
	private String pleitosCobranzas;
	private String poderesEspeciales;
	public String getApoderados() {
		return apoderados;
	}
	public void setApoderados(String apoderados) {
		this.apoderados = apoderados;
	}
	public String getActosDominio() {
		return actosDominio;
	}
	public void setActosDominio(String actosDominio) {
		this.actosDominio = actosDominio;
	}
	public String getActosAdministracion() {
		return actosAdministracion;
	}
	public void setActosAdministracion(String actosAdministracion) {
		this.actosAdministracion = actosAdministracion;
	}
	public String getTitulosCredito() {
		return titulosCredito;
	}
	public void setTitulosCredito(String titulosCredito) {
		this.titulosCredito = titulosCredito;
	}
	public String getPleitosCobranzas() {
		return pleitosCobranzas;
	}
	public void setPleitosCobranzas(String pleitosCobranzas) {
		this.pleitosCobranzas = pleitosCobranzas;
	}
	public String getPoderesEspeciales() {
		return poderesEspeciales;
	}
	public void setPoderesEspeciales(String poderesEspeciales) {
		this.poderesEspeciales = poderesEspeciales;
	}
	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	
	

}
