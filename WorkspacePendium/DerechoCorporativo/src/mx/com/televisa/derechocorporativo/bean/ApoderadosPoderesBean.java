package mx.com.televisa.derechocorporativo.bean;

import java.io.Serializable;

public class ApoderadosPoderesBean implements Serializable {

	private static final long serialVersionUID = 1L;

	private int idMetaRow;
	private String fecha;
	private String asunto;
	private String escritura;
	private String semaforoEstatus;
	private String fecOtorgamiento;
	private String reqInsRppc;
    private String fecRegistro;
    private String semaforo;

    public ApoderadosPoderesBean( int idMetaRow
                                 ,String fecha
                                 ,String asunto
                                 ,String escritura
                                 ,String semaforoEstatus
                                 ,String fecOtorgamiento
                                 ,String reqInsRppc
                                 ,String fecRegistro
                                 ,String semaforo
    ){
    	this.idMetaRow = idMetaRow;
    	this.fecha = fecha;
    	this.asunto = asunto;
    	this.escritura = escritura;
    	this.semaforoEstatus = semaforoEstatus;
    	this.fecOtorgamiento = fecOtorgamiento;
    	this.reqInsRppc = reqInsRppc;
    	this.fecRegistro = fecRegistro;
    	this.semaforo = semaforo;
    }

	public int getIdMetaRow() {
		return idMetaRow;
	}

	public String getFecha() {
		return fecha;
	}

	public String getAsunto() {
		return asunto;
	}

	public String getEscritura() {
		return escritura;
	}

	public String getSemaforoEstatus() {
		return semaforoEstatus;
	}

	public String getFecOtorgamiento() {
		return fecOtorgamiento;
	}

	public String getReqInsRppc() {
		return reqInsRppc;
	}

	public String getFecRegistro() {
		return fecRegistro;
	}

	public String getSemaforo() {
		return semaforo;
	}

}