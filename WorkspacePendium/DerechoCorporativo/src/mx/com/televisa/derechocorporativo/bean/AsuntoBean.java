package mx.com.televisa.derechocorporativo.bean;

public class AsuntoBean {
	private int idAsuntoRow;
	private int idMetaRow;
	private int idEmpresa;
	private int idUser;
	private String idAsunto;
	private String asunto;
	
	public int getIdAsuntoRow() {
		return idAsuntoRow;
	}
	public void setIdAsuntoRow(int idAsuntoRow) {
		this.idAsuntoRow = idAsuntoRow;
	}
	public int getIdMetaRow() {
		return idMetaRow;
	}
	public void setIdMetaRow(int idMetaRow) {
		this.idMetaRow = idMetaRow;
	}
	public int getIdEmpresa() {
		return idEmpresa;
	}
	public void setIdEmpresa(int idEmpresa) {
		this.idEmpresa = idEmpresa;
	}
	public int getIdUser() {
		return idUser;
	}
	public void setIdUser(int idUser) {
		this.idUser = idUser;
	}
	public String getIdAsunto() {
		return idAsunto;
	}
	public void setIdAsunto(String idAsunto) {
		this.idAsunto = idAsunto;
	}
	public String getAsunto() {
		return asunto;
	}
	public void setAsunto(String asunto) {
		this.asunto = asunto;
	}
	
	
}
