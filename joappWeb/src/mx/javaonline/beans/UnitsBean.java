package mx.javaonline.beans;

import java.io.Serializable;
import java.util.List;

public class UnitsBean implements Serializable{

	private static final long serialVersionUID = 1L;
	private int unitId;
	private String unitName;
	private String link;
	private int courseId;
	private List<TopicsBean> listTopicName;

	public UnitsBean() {
		
	}
	
	

	public List<TopicsBean> getListTopicName() {
		return listTopicName;
	}



	public void setListTopicName(List<TopicsBean> listTopicName) {
		this.listTopicName = listTopicName;
	}



	public int getUnitId() {
		return unitId;
	}

	public void setUnitId(int unitId) {
		this.unitId = unitId;
	}

	public String getUnitName() {
		return unitName;
	}

	public void setUnitName(String unitName) {
		this.unitName = unitName;
	}

	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

	public int getCourseId() {
		return courseId;
	}

	public void setCourseId(int courseId) {
		this.courseId = courseId;
	}

	
}
